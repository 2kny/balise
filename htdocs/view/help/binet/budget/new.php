<h1>Tu t'apprêtes à créer une nouvelle ligne budgétaire.</h1>
<p>
  Indique son nom, les mots-clefs et le montant.<br>
  Les mots-clefs te permettront de faire une recherche parmi toutes tes lignes budgétaires, à toi d'être inventif.<br>
  Par exemple, si tu organises deux événements, tu peux mettre le nom (ou la date) en mots-clefs et tu pourras alors trier pour voir chaque événement séparement.
</p>
