<h1>Voici le détail d'une demande de subventions</h1>
<p>Tu t'apprêtes à distribuer des subventions, mais ce n'est qu'un brouillon, tu pourras encore le modifier par la suite avant de le publier<br>
  Il y a deux parties à cette demande de subventions :
  <ul>
    <li>Le résumé du binet</li>
    <li>Sa demande</li>
  </ul>
  Dans la première partie, tu as :
  <ul>
    <li>Le descriptif du binet</li>
    <li>La réponse à la question que tu as posée lors de la publication de la vague</li>
    <li>Le résumé de la trésorerie du binet ainsi que le détail des subventions qu'a touché ce binet</li>
  </ul>
  Si tu ne sais pas ce que fait ce binet, n'hésite pas à cliquer sur son nom pour jeter un coup d'oeil sur sa trésorerie !<br>

  Dans la deuxième partie, il faut que tu attribues les montants des subventions à chaque poste de dépense du binet.
  Il t'a fourni une justification de chaque dépense et tu as également un champ pour lui expliquer ta décision !
</p>
