<h1>Lien avec le budget</h1>
<p>Après avoir créé l'opération, il te faut la relier à tes lignes budgétaires.
    Tu peux affecter une opération à plusieurs lignes : le montant restant à attribuer est indiqué en bas!<br>

    C'est comme ça que tu débloques tes subventions : il suffit que tu assignes ton opération à une ligne subventionnée. (<strong>Si tu ne comptes pas répartir exactement les dépenses comme dans ta demande de subventions, n'hésite pas à demander au binet directement</strong>)
    N'oublie pas après d'aller apporter la facture à la Kès pour te faire rembourser !<br>

    Si tu n'as pas la ligne qui te convient, tu peux aller la créer, l'opération en attente ira dans l'onglet validation et tu pourras le faire plus tard.
</p>
