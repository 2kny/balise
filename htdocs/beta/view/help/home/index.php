<h1>Bienvenu sur la page d'accueil !</h1>
    <p>D'ici, tu peux :
      <ul>
        <li>Accéder à tes binets et les gérer en cliquant dessus</li>
        <li>Accéder à des informations sur l'ensemble des binets du plateau (la liste ou encore l'historique des vagues de subventions)</li>
      </ul>
			Durant ta navigation sur le site, tu pourras à tout moment revenir à cette page en cliquant en haut à gauche sur "Balise"<br>
Il y a deux boutons que tu dois remarquer sur ce site : le bouton "Aide" et le bouton "Plus" en haut à droite.<br>
L'aide varie en fonction des pages, n'hésite pas à t'y référer si tu as un souci ou une question.<br>
Si tu as fais une dépense pour un binet, tu peux le lui signaler via le bouton "Plus", en lui proposant une opération.
L'administrateur de ce binet validera ton opération ou non, par exemple après que tu lui as passé la facture.
		</p>
