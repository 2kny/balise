<h1>Tu t'apprêtes à créer une nouvelle opération</h1>
<p>Tu peux ainsi signaler à un binet que tu as fais une dépense pour lui.
  Apporte ensuite la facture au trésorier de ce binet et il validera ton opération.<br>
  Mets le plus d'informations possible, ça lui sera utile ! Et tu pourras être remboursé plus vite si tu indiques ton nom dans le champ 'Payé par'
</p>
