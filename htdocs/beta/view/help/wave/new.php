    <h1>Tu t'apprêtes à créer une nouvelle vague de subventions</h1>
    <p>Il faut pour ça que tu précises les informations suivantes :
      <ul>
        <li>La date de clotûre des demandes (attention, si tu indiques le 23, cela finira le 22 à 23:59)</li>
        <li>La date de clotûre de l'utilisation de ces subventions (la remarque précédente est valable)</li>
      </ul>
      Ces dates pourront être modifiées par la suite.<br>
   Il faut également que tu choisisses la question que tu veux poser aux binets.
   Par exemple, pourquoi êtes-vous éligible à cette vague, que compter vous faire de vos sous ...
   Ou encore, à quelle date comptez-vous utilisez votre subventions, quand se déroule votre événement ...
   À toi d'y réflechir pour avoir les renseignements qui t'intéressent.
   Tu peux même poser plusieurs questions à la suite dans ce champ<br>

   Un point important à savoir : les binets ayant rédigé un brouillon de demande de subventions pourront quand même te l'envoyer après la date de clotûre.
   Tu en seras informé, à toi de décider si c'est trop tard ou pas.
   <strong>Après avoir créé ta vague, elle sera à l'état de brouillon. Il te faudra encore l'ouvrir en cliquant sur le bouton "ouvrir".</strong>
    </p>
